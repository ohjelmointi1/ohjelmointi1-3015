package viikko02.toisto;

public class Toisto {

    public static void main(String[] args) {
        // alustus
        int nykyinen = 1;

        // toistoehto
        while (nykyinen <= 100) {
            System.out.println(nykyinen);

            // kasvatus
            nykyinen += 1;
        }
    }
}
